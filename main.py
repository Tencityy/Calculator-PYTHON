operator = input("Enter a a operator + - * /: ")
number1 = int(input("Enter your first number: "))
number2 = int(input("Enter your second number: "))

if operator == "+":
    result = number1 + number2
    print(f"Your Result is {result}")
elif operator == "-":
    result = number1 - number2
    print(f"Your Result is {result}")
elif operator == "*":
    result = number1 * number2
    print(f"Your Result is {result}")
elif operator == "/":
    result = number1 / number2
    print(f"Your Result is {result}")


